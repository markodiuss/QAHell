public class SomeTypesNewVersion {

    public static void main(String[] args) {

        double b = 2.6;

        int c = (int)(0.5 + b);

        System.out.println(c); // = 3

        System.out.println((int)9.69); //  = 9

        System.out.println((int)'A'); // 65 - code of symbol "A"

        System.out.println((double)3); // = 3.0

    }
}
